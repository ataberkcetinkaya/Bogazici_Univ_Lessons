let x = 6.2

console.log(Math.round(x)) //girilen sayiyi en yakin tam sayiya yuvarlar

console.log(Math.ceil(x)) //girilen sayiyi en yakin yukarisindaki tam sayiya yuvarlar

console.log(Math.floor(x)) //girilen sayiyi en yakin altindaki tam sayiya yuvarlar

console.log(Math.trunc(x)) //sayinin küsüratini görmezden gelir

